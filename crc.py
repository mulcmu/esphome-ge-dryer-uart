
def crc16(data: bytes) -> int:
    crc = 0xE300
    
    for b in data:
        crc ^= b << 8
    
        for j in range(0, 8):
            if crc & 0b1000000000000000:
                crc <<= 1
                crc ^= 0x1021
            else:
                crc <<= 1
    
            crc &= 0xFFFF
            
        print(hex(b) + ', ' , end='')
    
    print(hex(crc >> 8) + ", " + hex(crc & 0xFF) + ', 0xe3')


